import math


def sin(a):
    return math.sin(a)


def cos(a):
    return math.cos(a)


def tan(a):
    return math.tan(a)


def hyperbolic_sin(a):
    return math.sinh(a)


def hyperbolic_cos(a):
    return math.cosh(a)


def hyperbolic_tan(a):
    return math.tanh(a)


def arcsin(a):
    return math.asin(a)


def arccos(a):
    return math.acos(a)


def arctan(a):
    return math.atan(a)


def arctan2(a, b):
    return math.atan2(a, b)


def arccot(a):
    return math.atan(1 / a)


def arccsc(a):
    return math.asin(1 / a)


def arcsec(a):
    return math.acos(1 / a)


def hyperbolic_arcsin(a):
    return math.asinh(a)


def hyperbolic_arccos(a):
    return math.acosh(a)


def hyperbolic_arctan(a):
    return math.atanh(a)


def hypot(a, b):
    return math.hypot(a, b)


def pythagorean_theorem(a, b):
    return math.sqrt(a ** 2 + b ** 2)


def convert_radians_to_degrees(a):
    return math.degrees(a)


def convert_degrees_to_radians(a):
    return math.radians(a)


def euclidean_distance(a, b):
    return math.dist(a, b)
